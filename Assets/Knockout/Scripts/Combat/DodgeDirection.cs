namespace Knockout.Combat
{
    /// <summary>
    /// Enum defining dodge direction options.
    /// </summary>
    public enum DodgeDirection
    {
        /// <summary>
        /// Dodge to the left (relative to character facing).
        /// </summary>
        Left,

        /// <summary>
        /// Dodge to the right (relative to character facing).
        /// </summary>
        Right,

        /// <summary>
        /// Dodge backward (away from opponent).
        /// </summary>
        Back
    }
}
